using System.Collections;
using System.Collections.Generic;
using UnityEngine;
using Bounder.Framework;
#endregion
// ************************************************************************


// ************************************************************************ 
#region Class: ButtonPopperEvent
// ************************************************************************
public class ButtonPopperEvent : GameEvent 
{
	// ********************************************************************
	#region Public Data Members 
	// ********************************************************************
	public bool show = true;
	public string button;
	#endregion
	// ********************************************************************


	// ********************************************************************
	#region Constructors 
	// ********************************************************************
	public ButtonPopperEvent(string _button, bool _show = true)
	{
		button = _button;
		show = _show;
	}
	// ********************************************************************
	#endregion
	// ********************************************************************

}
#endregion
// ************************************************************************


// ************************************************************************ 
#region Class: ButtonPopper
// ************************************************************************
[RequireComponent(typeof(Animator))]
public class ButtonPopper : MonoBehaviour 
{
	// ********************************************************************
	#region Exposed Data Members 
	// ********************************************************************
	[SerializeField]
	private bool m_show = false;
	#endregion
	// ********************************************************************


	// ********************************************************************
	#region MonoBehaviour Methods 
	// ********************************************************************
	void Awake () 
	{
		Events.AddListener<ButtonPopperEvent>(OnButtonPopperEvent);
		if (m_show)
			gameObject.SetActive(true);
		GetComponent<Animator>().SetBool("Show", m_show);
	}
	// ********************************************************************
	void OnDestroy () 
	{
		Events.RemoveListener<ButtonPopperEvent>(OnButtonPopperEvent);
	}
	// ********************************************************************
	#endregion
	// ********************************************************************


	// ********************************************************************
	#region Public Methods 
	// ********************************************************************
	public void SetButtonInactive()
	{
		gameObject.SetActive(false);
	}
	// ********************************************************************
	#endregion
	// ********************************************************************


	// ********************************************************************
	#region Private Methods 
	// ********************************************************************
	private void OnButtonPopperEvent(ButtonPopperEvent _event)
	{
		if (_event.show != m_show && _event.button == name)
		{
			m_show = _event.show;
			if (m_show)
				gameObject.SetActive(true);
			GetComponent<Animator>().SetBool("Show", m_show);
		}
	}
	// ********************************************************************
	#endregion
	// ********************************************************************

}
#endregion
// ************************************************************************
