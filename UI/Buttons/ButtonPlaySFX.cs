#region Class: ButtonPlaySFX
// ************************************************************************
public class ButtonPlaySFX : MonoBehaviour 
{

	// ********************************************************************
	#region Exposed Data Members 
	// ********************************************************************
	[SerializeField]
	private AudioClip m_SFX;
	#endregion
	// ********************************************************************


	// ********************************************************************
	#region Public Methods 
	// ********************************************************************
	public void PlayClick () 
	{
		AudioManager.Play(m_SFX,AudioCategory.EFFECTS);
	}
	// ********************************************************************
	#endregion
	// ********************************************************************

}
