#region Imports
// ************************************************************************
using UnityEngine;
using Bounder.Framework;
#endregion
// ************************************************************************


// ************************************************************************ 
#region Class: ButtonPlaySFX
// ************************************************************************
public class ButtonPlaySFX : MonoBehaviour 
{

	// ********************************************************************
	#region Exposed Data Members 
	// ********************************************************************
	[SerializeField]
	private AudioClip m_SFX = null;
	#endregion
	// ********************************************************************


	// ********************************************************************
	#region Public Methods 
	// ********************************************************************
	public void PlayClick () 
	{
		AudioObject audio = AudioManager.Play(m_SFX,AudioCategory.EFFECTS);
		if (audio != null)
			audio.audioSource.ignoreListenerPause = true;
	}
	// ********************************************************************
	#endregion
	// ********************************************************************

}
#endregion
