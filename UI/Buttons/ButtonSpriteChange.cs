using System.Collections;
using System.Collections.Generic;
using UnityEngine;
using UnityEngine.UI;
#endregion
// ************************************************************************


// ************************************************************************ 
#region Class: ButtonSpriteChangeEvent
// ************************************************************************
public class ButtonSpriteChangeEvent : GameEvent 
{
	// ********************************************************************
	#region Public Data Members 
	// ********************************************************************
	public string button;
	public Sprite sprite;
	#endregion
	// ********************************************************************


	// ********************************************************************
	#region Constructors 
	// ********************************************************************
	public ButtonSpriteChangeEvent(string _button, Sprite _sprite)
	{
		button = _button;
		sprite = _sprite;
	}
	// ********************************************************************
	#endregion
	// ********************************************************************

}
#endregion
// ************************************************************************


// ************************************************************************ 
#region Class: ButtonSpriteChange
// ************************************************************************
public class ButtonSpriteChange : MonoBehaviour 
{

	// ********************************************************************
	#region Exposed Data Members 
	// ********************************************************************
	[SerializeField]
	private Image m_image = null;
	#endregion
	// ********************************************************************


	// ********************************************************************
	#region MonoBehaviour Methods 
	// ********************************************************************
	void Awake () 
	{
		Events.AddListener<ButtonSpriteChangeEvent>(OnButtonSpriteChangeEvent);
	}
	// ********************************************************************
	void OnDestroy () 
	{
		Events.RemoveListener<ButtonSpriteChangeEvent>(OnButtonSpriteChangeEvent);
	}
	// ********************************************************************
	#endregion
	// ********************************************************************


	// ********************************************************************
	#region Private Methods 
	// ********************************************************************
	private void OnButtonSpriteChangeEvent(ButtonSpriteChangeEvent _event)
	{
		if (_event.button == name)
		{
			m_image.sprite = _event.sprite;
		}
	}
	// ********************************************************************
	#endregion
	// ********************************************************************

}
#endregion
