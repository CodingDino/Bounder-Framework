// ************************************************************************ 
#region Class: StringExtension
// ************************************************************************
public static class StringExtension 
{

	// ********************************************************************
	#region Extension Methods 
	// ********************************************************************
	public static bool NullOrEmpty(this string _string)
	{
		return String.IsNullOrEmpty(_string);
	}
	// ********************************************************************
	#endregion
	// ********************************************************************

}
